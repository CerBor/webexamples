
var tom = {
   "name" : "Tom Cat",
   "age" : 3
};

// Or simple:
var jerry = {
    name : "Jerry Mouse",
    age : 3
};

// ------------- TEST ----------------------

console.log( tom["name"] ); // Tom Cat
console.log( tom.name );  // Tom Cat

console.log( jerry["name"] ); // Jerry Mouse
console.log( jerry.name ); // Jerry Mouse
